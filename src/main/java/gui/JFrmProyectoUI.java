    private void JBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnAgregarActionPerformed
        String actividad = JTxtFActividad.getText();
        Integer prioridad = (Integer) JSpnPrioridad.getValue();
        int insert = tree.insert(actividad, prioridad);
        if(insert == 1){
            listModel.addElement(nodeToString(actividad.trim(), prioridad));
            JTxtFActividad.setText("");
        }else if(insert == 0){
            JOptionPane.showMessageDialog(null, "No se puede insertar actividades nulas. "
                    + "Por favor, complete el campo.", "Error inserci√≥n", JOptionPane.WARNING_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(null, "No se puede insertar actividades repetidas",
                    "Error inserci√≥n", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JBtnAgregarActionPerformed

    private void JBtnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnOrdenarActionPerformed
        listModel.clear();
        for(String node : tree.inOrder()) {
            listModel.addElement(node);
        }
    }//GEN-LAST:event_JBtnOrdenarActionPerformed

    private void JBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnEliminarActionPerformed
        try{
            String Originalvalue = JListActividades.getSelectedValue();
            String [] values = Originalvalue.split("\\|");
            String valueAct = values[0].trim();
            int valuePrio = Integer.parseInt(String.valueOf(values[1].trim().charAt(11)));
            System.out.println(valueAct + " PRIO " + valuePrio);
            if(tree.deleteNode(valueAct, valuePrio)) {
                listModel.removeElement(Originalvalue);
            }else{
                JOptionPane.showMessageDialog(null, "Ha sucedido un error al eliminar el √≠tem "
                        + "seleccionado. Por favor, contacte al administrador.", "Error interno",
                        JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error. Se debe seleccionar alg√∫n √≠tem. ", 
                    "Error eliminaci√≥n", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JBtnEliminarActionPerformed

